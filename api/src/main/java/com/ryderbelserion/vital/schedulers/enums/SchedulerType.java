package com.ryderbelserion.vital.schedulers.enums;

/**
 * Enum representing different types of schedulers.
 *
 * @author Ryder Belserion
 * @version 0.1.0
 * @since 0.1.0
 */
public enum SchedulerType {

    /**
     * Global scheduler type.
     */
    global_scheduler,

    /**
     * Region scheduler type.
     */
    region_scheduler,

    /**
     * Entity scheduler type.
     */
    entity_scheduler,

    /**
     * Asynchronous scheduler type.
     */
    async_scheduler;

}